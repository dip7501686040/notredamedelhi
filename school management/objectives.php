<h3>FAITH IN GOD</h3>

(a) A deep inter-relationship with God that permeates his/her daily life and his/her contact with others.<br>
(b) Personal and group prayer to God whom he/she considers to be his/ her loving Father.<br>
(c) Acceptance of and respect for all religions.<br>
(d) Deepening and broadening of his/her religious convictions.<br>

<h3>CONSCIENTISATION TOWARDS JUSTICE AND EQUALITY</h3>

Every Notre Dame student must be imbued with the fact that she/he is living in a very unjust society 
and that she/he must do all within her/ his power to bring about a radical change in the present structures.
 Therefore, she/he must develop:<br>
(a) A genuine love for the poor and the needy. She/he must programme regular sessions during the week in which she/he
 has some positive contact and communications with the poor, the illiterate, and the needy.<br>
(b) A loving concern for the different types of oppressions suffered by the women, the minorities, the under
 paid labourers, and the weaker sections.<br>
(c) Outstanding speaking and writing ability so that she/he can use her/ his talents as a platform to speak
 and write against any injustice and oppression suffered by others.<br>
(d) A willingness to involve himself/herself in programmes, projects, camps, workshops, etc. in which she/he will 
be actively involved in helping the financially poor or deprived sections.
