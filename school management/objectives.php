<div class="five">
<h3>SENSE OF COMMUNITY</h3>

(a) Realising that he/she belongs to a country that is rich in cultural heritage, she/he develops patriotism and pride for India.<br>
(b) As part of the school family, she/he especially develops awareness of the needs of others and responds to their unspoken wishes/
demands/ requests by giving generously of her/his time, talents, and service.<br>
(c) She/he inculcates in herself/himself those social virtues which are a genuine response to others: responsibility, courtesy,
 honesty, punctuality, loyalty, obedience, womanliness/manliness, generosity, and diligence.<br>
(d) She/he integrates herself/himself readily with others of various social, religious, cultural and national backgrounds.<br>
(e) As a citizen of the world, she/he takes keen interest in what is happening in other countries and easily discusses and
 empathises with the social and economic situations prevalent in them.<br>
 
 <h3>ACADEMIC VALUES</h3>

(a) Acquisition of knowledge and sharpening of her/his memory, speed and accuracy.<br>
(b) Acquisition of the following skills: observing, recording, quoting, illustrating, demonstrating, analysing,
 outlining, summarising, integrating, evaluating and criticising.<br>
(c) Power of expression through public speaking: debates, dramatics, elocution, extemporaneous speeches, dance, music and creative writing.<br>
(d) An on-going quest for knowledge that will last throughout her/his life.<br>
(e) An inner discernment which guides her/him to choose the correct values even when there is a dichotomy between the
 home and the school training or between society and the school.<br>
(f) Physical exercises, sports and athletic skills which help her/him develop a sound mind in a sound body.<br>
