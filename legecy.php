	<div class="main">
		<div class="one">
			<b>Notre Dame School</b> strives to keep alive the legacy of <b>St. Julie Billiart</b>, her Spiritual Mother,who
			filled with faith in God and a burning desire to work for the transformation of society founded the
			congregation of Sisters of Notre Dame. St. Julie was born on July 12, 1751 into a middle-class family
			in Cuvilly, France. As a child she was healthy, intelligent, sensible, and hard-working.<br><br>

			During her teenage years misfortune struck the family. The Billiarts were reduced to poverty. Julie’s
			faith in the good God gave her the courage to work hard and earn a living for her family. Julie was
			seen as an enemy of the French Revolution because of her teaching and the support she gave to priests.
			She had to go into hiding to escape from being guillotined. Her health became fragile and gradually her
			whole body was paralysed. For twenty-three years Julie remained a victim of paralysis but she continued
			to instruct and counsel those who came to her bedside. A group of young women looked up to Julie as their
			spiritual guide and they became the members of the <b>Congregation of the Sisters of Notre Dame that Julie founded
				in Namur in 1804.</b>

		</div>
		<div class="two">
			After twenty-three years of severe paralysis, Julie was healed miraculously. With her newfound energy,
			Julie opened several schools in France and Belgium. She was acclaimed not only as an educator,but also
			as a holy person who manifested God’s love and compassion for all people.She died on April 8, 1816.
		</div>
		<div class="three">
			<b>Sister Mary Aloysia and Sister Mary Ignatia</b>, the founding members of the Sisters of Notre Dame in Coesfeld,
			Germany in 1850 were greatly inspired by the spirit and charism of St. Julie Billiart. From Germany, the
			Congregation spread to other parts of the world:Italy, Holland, England, the United States, Brazil, Indonesia,
			India, Korea, Papua New Guinea, Tanzania, Kenya, Uganda, Mozambique the Philippines, Vietnam, China, Nicaragua, and Peru.

			The Sisters committed themselves to sharing their faith in God and working for the education of children who were
			economically poor, believing that education was the best way to better their lives.
