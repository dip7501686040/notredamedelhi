<h3>Goodbye Sir Jose</h3>
Goodbyes are only for those who love with their eyes. Because, for those who love with their heart and soul,
 there is no such thing as separation! But for every beginning, there must be an end. It's paradoxical but that's how it is.<br><br>

<b>On January 18, 2020</b>, the entire Notre Dame family gathered to bid farewell to an exceptional teacher, an altruistic human being, Sir 
Jose. The beauty of the stage was enhanced by an aesthetically pleasing backdrop and the lamp waiting to be lighted. The amazing 
programme began with a prayer song and a prayer dance. The students were beautifully dressed and they presented a number of dance
 performances. Students presented some skits, followed by speeches appreciating the nature of Sir Jose.<br><br>

This sense of humour and his dressing sense would be adamantly admired by everyone. He would be remembered by many students
 as a guide and mentor who helped shape their lives for the better. He is extremely charismatic and knew how to interact with
 his students on a personal level. He knew the art of adding charm to even the most boring lesson.<br><br>

His finesse and his passion for literature gave him an unmatched ability to add life to mere fictional characters. 
The programme ended with a song by the choir, melodiously sung.<br><br>

Lastly, Sir Jose expressed his feelings, his experiences and his journey in Notre Dame School after which Sr.
 Alice appreciated Sir and blessed the gathering. The day was great, filled with nostalgia, fun and excitement. 
 Saying good bye is never easy. But, that is a bitter truth of life.<br><br>
<h3>Christmas Celebration</h3>
<h4><b>"Joy to the world<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The Lord has come!"</b></h4>

Christmas is the festival which inspires the spirit if sharing and caring. Soaking in the spirit of Christmas, the students of Notre
 Dame School celebrated the festival of Christmas with enthusiasm on the <b>21st of December 2019.</b><br><br>

Leaving no stone unturned to sprinkle the magic of their love, the stage was wholly decorated by the students.
 The programme started with a prayer dance followed by a beautifully staged skit, directed by Sir Jose, depicting
 the significance of the festival, showcasing the life and journey of Jesus Christ.<br><br>

The elements of energy and momentum could be witnessed in the children as they came forward and presented their 
items. Lastly the much awaited Santa Claus entered the stage with his elves and reindeers, boasting up the energy
 level of the students. A brief history of Santa Claus was depicted through dance and narrations. The programme 
 ended with the choir singing melodious carols.<br><br>

The Principal, Sr. Mary Alice threw light on the importance of the day and blessed the gathering with her inspirational words.
