<h3>Children's Day Celebration</h3>
"Children are not things to be moulded, but are people to be unfolded." - Jess Lair<br><br>

Children's Day is celebrated to commemorate the birth anniversary of "Chacha Nehru", the first Prime Minister of India.<br><br>

This year as the pollution level crossed limits in Delhi, the schools were closed on the 14th of November. Therefore, Children's
 day was celebrated on the <b>20th of November</b> in Notre Dame School, Badarpur.<br><br>

The school glowed with the bright smiles of the children and the stage looked bright and beautiful as the teachers dressed 
up in attractive attires. The Children's day programme began with a melodious song sung by the teachers, followed by a play 
staged in a very humorous manner which laid emphasis on the importance of education. The programme ended with a beautiful
 dance performance by the senior and the primary teachers and with the blessings of our Principal Sr.Mary Alice. The students 
 were given some energizing refreshments in their respective classes. The day was a special and enjoyable one for all!!<br><br>
 <h3>STUDENT CULTURAL EXCHANGE PROGRAM 2019</h3>
 The student cultural program is an innovative initiative by the CBSE to promote healthy and amicable environment amongst 
 students of two schools. It not only unites two school groups but also promotes friendship in an effective way to build 
 bridges among students from different backgrounds.<br><br>

Under this program Notre Dame School Badarpur and GBSSS No 1 Badarpur, had their cultural exchange on 22October and 24 October 2019.
 On the 22nd , 30 boys from Notre Dame School visited GBSSS No1 Badarpur, where they were welcomed by the Principal, Staff and students
 with lot of warmth and affection. The students of both the schools exhibited their talent through skit, dance , mime and a patriotic song.
 Mr Shailender , Cluster Resource Centre Coordinator encouraged the students by his motivating speech and also taught a song to the students
 of both the schools. Students of both the schools interacted with each other and shared food amongst themselves.<br><br>

In the second spell the gracious Notre Dame family received neighbouring school guests with lot of warmth and affection on the 24th of October
 2019 by our school Principal Sr Mary Alice , staff members and other students with the school band which made it more special for them. As
 a tradition of Notre Dame School, the program started with a solemn prayer song and a welcome speech by the Principal. It was followed by 
 patriotic song by the students of Notre Dame School. The guest school presented few cultural programs like action song, skits on social issues
 as 'Beti Bachao Beti Padao Yojna'. They beautifully explained how education and employment of women is essential for the progress of our country. 
 Students also discussed how plastic is damaging our environment. Few games were also organized in which children demonstrated exemplary spirit
 and had great fun. The students let their taste buds experience and relish the Indian delicacies with much taste and fun.<br><br>

At the conclusion of the program, Mr B N Dubey , Cluster Coordinator blessed the gathering and appreciated the exchange program and was 
overwhelmed by the hospitality and participation of both the schools. As a token of love and to greet Diwali wishes, the host school presented 
handmade card and diyas to the guest students and teachers.<br>
The program was a milestone in our personal and professional journey of knowing each other. This report cannot be concluded without
 mentioning the unstinted support and involvement of : Mr B N Dubey, Mr Shailendar, Dr N P Sharma and Sr Mary Alice.<br>
We wish this relationship between Notre Dame and GBSSS No 1 Badarpur to grow from strength to strength with each passing year.<br>
