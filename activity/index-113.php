				<a href="index-1.htm?p=760" title="Permalink to Pre-Primary Annual Day Report" rel="bookmark">Graduation Ceremony and Farewell</a>
			</h1>
								</header><!-- .entry-header -->
								

				<div class="entry-content">
				<p>
“Twenty years from now you will be more disappointed by the things you didn’t do than by the ones you did do. So throw off the bowlines. Sail away from the safe harbour. Catch the trade winds in your sails. Explore, Dream, Discover.”  
                                                                                                                                                              -Mark Twain
</p>
			<p>On <strong>17th of December 2019,</strong>
 the Senior classes were filled with enthusiasm as the Class XII1 students were to have their Graduation Ceremony and Farewell programme.&#8221;.</p>

					</div><!-- .entry-content -->
		
		<!-- <footer class="entry-meta"> -->
			<!-- This entry was posted in <a href="index-2.htm?cat=3" title="View all posts in General" rel="category">General</a> on <a href="index-1.htm?p=760" title="3:42 pm" rel="bookmark"><time class="entry-date" datetime="2019-11-30T15:42:29+00:00">November 30, 2019</time></a><span class="by-author"> by <span class="author vcard"><a class="url fn n" href="index-3.htm?author=1" title="View all posts by ndsuser" rel="author">ndsuser</a></span></span>.								</footer><!-- .entry-meta --> 
	</article><!-- #post -->

				

				<div class="entry-content">
	
<p style="margin-top: -18%;">The Graduation Ceremony started with the lighting of the lamp and a melodious song by Sir Winston. The parents were made to sit in the auditorium. Sangeeta Ma’am addressed the gathering which was followed by the students sharing their 14 year experience in the school and thanking everyone who helped them in every possible way. Parents also expressed their gratitude towards the school for nurturing their kids.</p>
<p>The ceremony ended with the blessings of our Principal Sr. Mary Alice and distribution of mementos. The students, parents and teachers were given refreshments too.</p>
<p>All the students of Std. XI and XII looked gorgeous in their saris and suits. Class XI was very much excited to bid farewell to their seniors.</p>
<p>The programme started with a comic skit followed by a good number of dance performances presented by Class XI.</p>
<p>There were some interesting titles which were given away and the most awaited and prestigious titles of “Miss Notre Dame” and “Mr. Notre Dame” were bagged by Sandra Simon of XII B and Soumya Ranjan Patnaik of XII A.</p>
<p>The day ended with all the students enjoying themselves on the stage, dancing.</p>
